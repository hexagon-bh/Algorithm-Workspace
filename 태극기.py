import turtle
t=turtle.Turtle()
t.shape("turtle")
t.color("red")
t.left(90)
t.begin_fill()
t.circle(100,180)
t.circle(50,-180)
t.left(180)
t.circle(50,180)
t.end_fill()
t.color("blue")
t.begin_fill()
t.circle(100,-180)
t.circle(50,-180)
t.left(180)
t.circle(50,180)
t.end_fill()

t.penup()
t.setposition(30,40)
t.right(50)
t.pendown()
for i in range(2):
    t.forward(20)
    t.left(90)
    t.forward(100)
    t.left(90)

t.penup()
t.setposition(-230,40)
t.right(50)
t.pendown()
for i in range(2):
    t.forward(20)
    t.right(90)
    t.forward(100)
    t.right(90)

turtle.mainloop()
